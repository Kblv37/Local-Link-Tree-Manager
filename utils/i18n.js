let _lang = 'en';

export function setLanguage(lang) {
    _lang = (lang === 'ru') ? 'ru' : 'en';
}

export function getLanguage() { return _lang; }

export function t(key, ...args) {
    const msg = MESSAGES[_lang]?.[key] ?? MESSAGES.en[key] ?? key;
    if (typeof msg === 'function') return msg(...args);
    return msg;
}

export function applyI18nToDOM() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const msg = MESSAGES[_lang]?.[key] ?? MESSAGES.en[key];
        if (msg && typeof msg === 'string') el.textContent = msg;
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        const msg = MESSAGES[_lang]?.[key] ?? MESSAGES.en[key];
        if (msg && typeof msg === 'string') el.placeholder = msg;
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.getAttribute('data-i18n-title');
        const msg = MESSAGES[_lang]?.[key] ?? MESSAGES.en[key];
        if (msg && typeof msg === 'string') el.title = msg;
    });
    document.querySelectorAll('option[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const msg = MESSAGES[_lang]?.[key] ?? MESSAGES.en[key];
        if (msg && typeof msg === 'string') el.textContent = msg;
    });
}